package org.college;

public class CharacterIndex {

	public static void main(String[] args) {
		String text = "Java Exercise";
		int index1 = text.indexOf('E');
		int index2 = text.indexOf('s');
		System.out.println("index of E is : " + index1);
		System.out.println("index of s is : " + index2);

	}

}
